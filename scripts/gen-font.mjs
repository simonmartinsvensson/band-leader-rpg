// --- Minimal PNG encoder (8-bit RGBA) -----------------------------------------
const crcTable = (() => {
  const t = new Uint32Array(256);
  for (let n = 0; n < 256; n++) {
    let c = n;
    for (let k = 0; k < 8; k++) c = c & 1 ? 0xedb88320 ^ (c >>> 1) : c >>> 1;
    t[n] = c >>> 0;
  }
  return t;
})();
const crc32 = (buf) => {
  let c = 0xffffffff;
  for (let i = 0; i < buf.length; i++) c = crcTable[(c ^ buf[i]) & 0xff] ^ (c >>> 8);
  return (c ^ 0xffffffff) >>> 0;
};
const chunk = (type, data) => {
  const t = Buffer.from(type, "ascii");
  const len = Buffer.alloc(4);
  len.writeUInt32BE(data.length, 0);
  const crc = Buffer.alloc(4);
  crc.writeUInt32BE(crc32(Buffer.concat([t, data])), 0);
  return Buffer.concat([len, t, data, crc]);
};
const encodePNG = (w, h, rgba) => {
  const sig = Buffer.from([137, 80, 78, 71, 13, 10, 26, 10]);
  const ihdr = Buffer.alloc(13);
  ihdr.writeUInt32BE(w, 0);
  ihdr.writeUInt32BE(h, 4);
  ihdr[8] = 8;
  ihdr[9] = 6;
  const stride = w * 4;
  const raw = Buffer.alloc((stride + 1) * h);
  for (let y = 0; y < h; y++) rgba.copy(raw, y * (stride + 1) + 1, y * stride, y * stride + stride);
  return Buffer.concat([sig, chunk("IHDR", ihdr), chunk("IDAT", deflateSync(raw)), chunk("IEND", Buffer.alloc(0))]);
};

// --- Render the atlas ---------------------------------------------------------
const count = LAST_CODE - FIRST_CODE + 1;
const rows = Math.ceil(count / CHARS_PER_ROW);
const W = CHARS_PER_ROW * CELL_W;
const H = rows * CELL_H;
const buf = Buffer.alloc(W * H * 4); // transparent

const setPx = (x, y) => {
  const i = (y * W + x) * 4;
  buf[i] = 255;
  buf[i + 1] = 255;
  buf[i + 2] = 255;
  buf[i + 3] = 255;
};

for (let i = 0; i < count; i++) {
  const ch = String.fromCharCode(FIRST_CODE + i);
  const glyph = G[ch];
  if (!glyph) continue;
  const cellX = (i % CHARS_PER_ROW) * CELL_W;
  const cellY = Math.floor(i / CHARS_PER_ROW) * CELL_H;
  for (let gy = 0; gy < GLYPH_H; gy++) {
    const rowStr = glyph[gy] ?? "";
    for (let gx = 0; gx < GLYPH_W; gx++) {
      if (rowStr[gx] === "#") setPx(cellX + gx, cellY + gy);
    }
  }
}

mkdirSync(ASSET_DIR, { recursive: true });
const out = resolve(ASSET_DIR, "font.png");
writeFileSync(out, encodePNG(W, H, buf));
console.log(`Wrote ${out} (${W}x${H}, ${count} glyphs, ${CELL_W}x${CELL_H} cells)`);
